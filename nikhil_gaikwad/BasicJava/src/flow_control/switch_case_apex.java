package flow_control;

public class switch_case_apex {
    public static void main(String[] args) {

        for(int i=1985;i<=2001;i++){
            switch(i){
                case 2001:
                    System.out.println("birth date of akshay");
                    break;
                case 2000:
                    System.out.println("birth date of nikhil");
                    break;
                case 1999:
                    System.out.println("birth date of balasaheb");
                    break;
                case 1998:
                    System.out.println("birth date of ajay");
                    break;
                case 1996:
                    System.out.println("birth date of shubham");
                    break;
                case 1995:
                    System.out.println("birth date of pandu");
                    break;
                case 1985:
                    System.out.println("birth date of sandip");
                    break;
               default:
                 System.out.println("you choose wrong no");
            }
        }
    }
}
