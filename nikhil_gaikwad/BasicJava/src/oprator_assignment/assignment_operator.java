package oprator_assignment;

public class assignment_operator {
    public static void main(String[] args) {
        //create variable
        int a=4;
        int var;
        //assign value using=
        var=a;
        System.out.println("var using =:"+var);
        //assign value using +=
        var+=a;
        System.out.println("var using+=:"+var);
        //assign value using *=
        var*=a;
        System.out.println("var using*=:"+var);

    }
}
