package simple.java.com;

public class swap_two_no {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("before swap two no"+a+" "+b);
        int t;
        t=a;
        a=b;
        b=t;
        System.out.println("after swap two no"+a+" "+b);
    }
}
